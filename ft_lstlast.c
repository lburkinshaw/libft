#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*ptr;

	ptr = lst;
	while (ptr != '\0')
	{
		if (!ptr -> next)
			return (ptr);
		ptr = ptr -> next;
	}
	return (ptr);
}
